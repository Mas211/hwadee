package com.jxt.entity;

//成绩
public class Grade {
	private Integer gradeId;
	private Integer gradeTd;
	private Integer gradeStuId;
	private Integer grade;
	
	public Integer getGradeId() {
		return gradeId;
	}
	public void setGradeId(Integer gradeId) {
		this.gradeId = gradeId;
	}
	public Integer getGradeTd() {
		return gradeTd;
	}
	public void setGradeTd(Integer gradeTd) {
		this.gradeTd = gradeTd;
	}
	public Integer getGradeStuId() {
		return gradeStuId;
	}
	public void setGradeStuId(Integer gradeStuId) {
		this.gradeStuId = gradeStuId;
	}
	public Integer getGrade() {
		return grade;
	}
	public void setGrade(Integer grade) {
		this.grade = grade;
	}
}
