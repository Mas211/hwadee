package com.jxt.entity;

public class Parent {
	private Integer parId;
	private String parName;
	private String parPassword;
	private Integer parAuthLevel;
	private Integer parStuId;
	private Integer parPhone;
	private String parAddress;
	private String parSex;
	
	public Integer getParId() {
		return parId;
	}
	public void setParId(Integer parId) {
		this.parId = parId;
	}
	public String getParName() {
		return parName;
	}
	public void setParName(String parName) {
		this.parName = parName;
	}
	public String getParPassword() {
		return parPassword;
	}
	public void setParPassword(String parPassword) {
		this.parPassword = parPassword;
	}
	public Integer getParAuthLevel() {
		return parAuthLevel;
	}
	public void setParAuthLevel(Integer parAuthLevel) {
		this.parAuthLevel = parAuthLevel;
	}
	public Integer getParStuId() {
		return parStuId;
	}
	public void setParStuId(Integer parStuId) {
		this.parStuId = parStuId;
	}
	public Integer getParPhone() {
		return parPhone;
	}
	public void setParPhone(Integer parPhone) {
		this.parPhone = parPhone;
	}
	public String getParAddress() {
		return parAddress;
	}
	public void setParAddress(String parAddress) {
		this.parAddress = parAddress;
	}
	public String getParSex() {
		return parSex;
	}
	public void setParSex(String parSex) {
		this.parSex = parSex;
	}
}
